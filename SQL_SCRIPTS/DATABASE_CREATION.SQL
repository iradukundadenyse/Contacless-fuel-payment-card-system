
-- CREATE CUSTOMER TABLE

CREATE TABLE CUSTOMER (
    Customer_ID NUMBER PRIMARY KEY,
    Full_Name VARCHAR2(50),
    Phone VARCHAR2(20),
    Email VARCHAR2(50)
);


-- CREATE PAYMENT CARD TABLE

CREATE TABLE PAYMENT_CARD (
    Card_ID NUMBER PRIMARY KEY,
    Customer_ID NUMBER,
    Card_Number VARCHAR2(20) UNIQUE,
    Balance NUMBER,
    Status VARCHAR2(20),
    FOREIGN KEY(Customer_ID) REFERENCES CUSTOMER(Customer_ID)
);


-- CREATE FUEL TRANSACTION TABLE

CREATE TABLE FUEL_TRANSACTION (
    Transaction_ID NUMBER PRIMARY KEY,
    Card_ID NUMBER,
    Fuel_Type VARCHAR2(20),
    Amount NUMBER,
    Transaction_Date DATE,
    FOREIGN KEY(Card_ID) REFERENCES PAYMENT_CARD(Card_ID)
);
CREATE TABLE AUDIT_LOG(
Audit_ID NUMBER PRIMARY KEY,
Action_Type VARCHAR2(50),
Username VARCHAR2(50),
Action_Date DATE
);
CREATE TABLE HOLIDAY(
Holiday_ID NUMBER PRIMARY KEY,
Holiday_Date DATE,
Description VARCHAR2(50)
);
CREATE SEQUENCE AUDIT_SEQ
